// while loop example

let count = 1;  
while (count <= 5) {
    console.log(count);
    count++;
}

let myArray = ['ironman', 'hulk', 'thor', 'black widow'];
let index = 0;
while (index < myArray.length) {
    console.log(myArray[index]);
    index++;
}

// do...while loop example

let num = 1;
do {
    console.log(num);
    num++;
} while (num <= 5);